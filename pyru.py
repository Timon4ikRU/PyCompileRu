# === КЛАССЫ НЕОБХОДИМЫЕ ДЛЯ НЕКОТОРЫХ ФУНКЦИЙ ===
class ПрерватьЦикл(Exception):
    pass

# === ОСНОВНЫЕ ФУНКЦИИ ===
def печатать(*args, sep=' ', end='\n'):
    """Аналог print()"""
    print(*args, sep=sep, end=end)

def прервать():
    """Аналог break для выхода из циклов"""
    raise ПрерватьЦикл()

def ввести(подсказка=''):
    """Аналог input()"""
    return input(подсказка)

def тип(объект):
    """Аналог type()"""
    return type(объект)

def длина(объект):
    """Аналог len()"""
    return len(объект)

def диапазон(start, stop=None, step=1):
    """Аналог range()"""
    if stop is None:
        return range(start)
    return range(start, stop, step)

# === ПРЕОБРАЗОВАНИЕ ТИПОВ ===
def строка(значение):
    return str(значение)

def целое(значение):
    return int(значение)

def дробное(значение):
    return float(значение)

def логическое(значение):
    return bool(значение)

def список(итерируемый=None):
    return list(итерируемый) if итерируемый is not None else []

def кортеж(итерируемый=None):
    return tuple(итерируемый) if итерируемый is not None else ()

def словарь(итерируемый=None):
    return dict(итерируемый) if итерируемый is not None else {}

def множество(итерируемый=None):
    return set(итерируемый) if итерируемый is not None else set()

# === РАБОТА СО СПИСКАМИ ===
def добавить(список, элемент):
    список.append(элемент)
    return список

def удалить(список, элемент):
    список.remove(элемент)
    return список

def найти(список, элемент):
    try:
        return список.index(элемент)
    except ValueError:
        return -1

def отсортировать(список, обратно=False):
    return sorted(список, reverse=обратно)

# === РАБОТА СО СЛОВАРЯМИ ===
def ключи(словарь):
    return list(словарь.keys())

def значения(словарь):
    return list(словарь.values())

def элементы(словарь):
    return list(словарь.items())

def получить(словарь, ключ, по_умолчанию=None):
    return словарь.get(ключ, по_умолчанию)

# === МАТЕМАТИКА ===
def сумма(итерируемый):
    return sum(итерируемый)

def максимум(*args):
    return max(*args)

def минимум(*args):
    return min(*args)

def абсолютное(число):
    return abs(число)

def округлить(число, знаки=0):
    return round(число, знаки)

# === ФАЙЛЫ ===
def открыть(путь, режим='r', кодировка='utf-8'):
    return open(путь, режим, encoding=кодировка)

def прочитать(файл):
    return файл.read()

def записать(файл, данные):
    return файл.write(данные)

def закрыть(файл):
    файл.close()

# === ДОПОЛНИТЕЛЬНЫЕ ===
def перечислить(итерируемый):
    """Аналог enumerate()"""
    return enumerate(итерируемый)

def объединить(итерируемый1, итерируемый2):
    """Аналог zip()"""
    return zip(итерируемый1, итерируемый2)

def фильтр(функция, итерируемый):
    """Аналог filter()"""
    return filter(функция, итерируемый)

def карта(функция, итерируемый):
    """Аналог map()"""
    return map(функция, итерируемый)

